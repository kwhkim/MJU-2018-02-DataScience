#60140609 10_김영빈

##데이터 이름 : "서울 가구원 조사" 서울서베이

##데이터 설명 : 서울 가구원을 대상으로 설문한 것으로 한국의 중심이고 수도인 서울의 가구원뿐만아니라 연령, 직업, 소득, 종교 등이 있어 다양한 분석이 용이합니다.

##데이터 선정 이유 : 대한민국의 가장 인구가 많은 서울의 가구원 조사에서는 많은 데이터들이 있기때문에 선정하였습니다. 또한 현재 서울에서 살고 있고, 서울에 회사 본사가 있고, 가장 한국을 표본적으로 잘 나타낼 수 있을 것이라고 생각하여 선정하였습니다.

##데이터 분석 방법 : 여러 방대한 데이터 중 데이터를 선별하고, 흥미로운 분석 결과가 도출되는 분석을 중심으로 데이터를 통계분석할 것입니다.

